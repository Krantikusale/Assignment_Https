package day4;

public class For {

	public static void main(String[] args) {
		for(int i1 =0;i1<10;i1++)//loop where value increment by 1
			System.out.println(i1);
		System.out.println("---------------");
		
		for(int i=0;i<10;i=i+1)//loop where value increment by 1
		{System.out.println("Java"+i);}
		System.out.println("done");
		System.out.println("---------------");
		 
		for(int i=0;i<10;i=i+2)//loop where value increment by 2
		{System.out.println("Java"+i);}
		System.out.println("done");
	}

}
