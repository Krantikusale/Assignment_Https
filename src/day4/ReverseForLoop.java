package day4;

public class ReverseForLoop {

	public static void main(String[] args) {
		for(int i=10;i>0;i--)
		{System.out.println(i);}
		System.out.println("------------");
		
		for(int i=10;i>0;i-=1)
		{System.out.println(i);}
		System.out.println("------------");

		for(int i=10;i>0;i=i-2)
		{System.out.println(i);}
		System.out.println("------------");


	}

}
