package day4;

public class NestedWhileLoop {

	public static void main(String[] args) {
		int a = 10;
		while (a > 0) {
			System.out.println("value of a is : "+a);
			a--;
		}

		System.out.println("******************************");
		int b = 0;
		while (b < 10) {
			System.out.println("value of b is : "+b);
			b++;
		}
	}

}
